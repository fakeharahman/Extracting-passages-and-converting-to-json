# returns the ans number (1,2,3,4) given question and id
def getAns(id, qno):
    ans=anss[id]
    ansnl=ans.split("\n")
    ans=[i.split(" ") for i in ansnl] 
    # print(ans)
    if(qno=="1"):
        return ans[0][ans[0].index('1')+1]
    if(qno=="2"):
        return ans[1][ans[1].index('2')+1]
    if(qno=="3"):
        return ans[2][ans[2].index('3')+1]
    if(qno=="4"):
        return ans[3][ans[3].index('4')+1]
    else:
        for i in ans:
            if qno in i:
                return i[i.index(qno)+1]

